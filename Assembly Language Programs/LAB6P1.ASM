;PROGRAM TO SWAP TWO NUMBERS:
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
VAR1 DB 'SWAP NUMBERS: $'     ;VARIABLE DECLARATION
VAR2 DB 'ENTER NUMBER: $'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET VAR2          ;IT WILL PRINT A STRING "ENTER NUMBER: "
MOV AH,9
INT 21H
MOV AH,1                    ;FIRST NUMBER INPUT
INT 21H
MOV BX,AX                   ;INPUT NUMBER GOES TO BX
PUSH BX                     ;PUSH BX IN STACK
MOV AH,1                    ;SECOND NUMBER GOES TO CX
INT 21H
MOV CX,AX                   ;INPUT NUMBER GOES TO CX
PUSH CX                     ;PUSH CX IN STACK

MOV DX,10                   ;LINE FEED
MOV AH,2
INT 21H

MOV DX,OFFSET VAR1          ;IT WILL PRINT A STRING "SWAP NUMBERS: "
MOV AH,9
INT 21H
POP BX                      ;POP VALUE FROM STACK AND STORE INTO BX
MOV DX,BX                   ;TO PRINT BX
MOV AH,2
INT 21H
POP CX                      ;POP VALUE FROM STACK AND STORE INTO CX
MOV DX,CX                   ;TO PRINT CX
MOV AH,2
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
