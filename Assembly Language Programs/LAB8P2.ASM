;PROGRAM TO PRACTICE MULTIPLICATION:
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
A DB 'ANSWER: $'        ;INITIALIZING STRING
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET A         ;TO PRINT STRING 'ANSWER: '
MOV AH,9
INT 21H
MOV AL,5                ;MOVING 5 TO AL
MOV BL,9                ;MOVING 9 TO BL
MUL BL                  ;MULTIPLYING
AAM                     ;ASCII ADJUST AFTER MULTIPLICATION
MOV CH,AH               ;MOVING VALUE FROM CH TO AH
MOV CL,AL               ;MOVING VALUE FROM AL TO CL
MOV DL,CH               ;MOVING VALUE FROM CH TO DL
ADD DL,48               ;ADDING 48(ASCII OF 0) T0 DL
MOV AH,2                ;TO PRINT THE VALUE
INT 21H
MOV DL,CL               ;MOVING VALUE FROM CL TO DL
ADD DL,48               ;ADDING 48(ASCII OF 0) TO DL
MOV AH,2                ;TO PRINT
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
