;PRACTICE CODE OF DIVISION:
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
QOU DW ?                  ;INITIALIZING VARIABLES
REM DW ?
A1 DB 'ANSWER: $'         ;DECLARING VARIABLES
A2 DB 'QOUTIENT: $'
A3 DB 'REMAINDER: $'
A4 DB '  &  $'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET A1          ;PRINT STRING 'ANSWER: '
MOV AH,9
INT 21H

MOV AX,21                 ;MOVING 21 INTO AX AS DIVIDEND
MOV DX,0                  ;FOR EMPTYING DX,MOVING ZERO INTO IT
MOV BX,5                  ;MOVING 5 INTO BX AS DIVISOR
DIV BX                    ;DIVIDE 21 BY 5

MOV QOU,AX                ;MOVE QOUTIENT VALUE FROM AX INTO QOU
ADD QOU,48                ;ADDING ASCII OF ZERO INTO QOU
MOV REM,DX                ;MOVE REMAINDER FROM DX INTO REM
ADD REM,48                ;ADDING ASCII OF ZERO INTO REM
MOV DX,OFFSET A2          ;TO PRINT STRING 'QOUTIENT: '
MOV AH,9
INT 21H
MOV DX,QOU                ;PRINT QOUTIENT VALUE
MOV AH,2
INT 21H

MOV DX,OFFSET A4          ;TO PRINT STRING '  &  '
MOV AH,9
INT 21H
MOV DX,OFFSET A3          ;TO PRINT STRING 'REMAINDER: ;
MOV AH,9
INT 21H
MOV DX,REM                ;PRINT REMAINDER VALUE
MOV AH,2
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
