;PROGRAM TO TAKE INPUT A NUMBER AND DISPLAY WHETHER THE NUMBER IS EVEN OR ODD:
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
VAR1 DB 'ENTER A NUMBER: $'  ;INITIALIZING STRINGS
VAR2 DB 'NUMBER IS EVEN$'
VAR3 DB 'NUMBER IS ODD$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET VAR1           ;TO PRINT STRING 'ENTER A NUMBER: '
MOV AH,9
INT 21H
MOV AH,1                     ;TAKE INPUT
INT 21H
MOV BL,2                     ;MOVING 2 INTO BL
DIV BL                       ;DIVIDING INPUT VALUE BY 2
CMP AH,0                     ;COMPARING THE VALUE IN AH WITH 0
JE L1                        ;IF BOTH VALUES ARE EQUAL THEN JUMP TO L1
MOV DX,10                    ;ENTER KEY
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
MOV DX,OFFSET VAR3           ;TO PRINT STRING 'NUMBER IS ODD'
MOV AH,9
INT 21H
JMP EXIT                     ;JUMP TO EXIT
L1:                          ;LABEL OF CHUNK OF CODE (L1)
MOV DX,10                    ;ENTER KEY
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
MOV DX,OFFSET VAR2           ;TO PRINT STRING 'NUMBER IS EVEN'
MOV AH,9
INT 21H
JMP EXIT                     ;JUMP TO EXIT
EXIT:                        ;LABEL OF CHUNK OF CODE (EXIT)
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
