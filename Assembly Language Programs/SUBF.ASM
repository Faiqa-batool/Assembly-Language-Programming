;PROGRAM TO SUBTRACT TWO NUMBERS
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC         ;MAIN PROGRAM STARTS
MOV BL,5          ;MOVE VALUE OF 5 IN BL
MOV CL,1          ;MOVE VALUE OF 1 IN CL
SUB BL,CL         ;SUBTRACT CL FROM BL AND MOVE IN BL
ADD BL,48         ;ADD 48(ASCII of zero) IN BL AND MOVE IN DL 
MOV DL,BL         ;MOVE VALUE OF BL IN DL
MOV AH,2
INT 21H           ;PRINT OUTPUT (SINGLE CHARACTER)
MOV AH,4CH
INT 21H
MAIN ENDP         ;MAIN PROCEDURE ENDS
END MAIN          ;PROGRAM ENDS
