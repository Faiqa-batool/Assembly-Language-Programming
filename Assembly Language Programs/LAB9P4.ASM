;PRACTICE CODE OF MACRO:

PRINT MACRO P1                    ;CODING  MACRO FOR PRINTING A STRING
MOV DX,OFFSET P1
MOV AH,9
INT 21H
ENDM

DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
A1 DB '-----FRIENDS-----$'                  ;ASSIGNED VARIABLES
A2 DB 'IT IS A SQUAD OF FOUR FRIENDS$'
A3 DB 'AINA JAWED$'
A4 DB 'AREEBA AMJAD$'
A5 DB 'FAIQA BATOOL$'
A6 DB 'MAHAM TAHIR$'
.CODE
MAIN PROC                     ;MAIN PROCEDURE
MOV AX,@DATA
MOV DS,AX
CALL ENTERKEY                 ;CALLING ENTERKEY PROCEDURE INSTEAD OF
                               ;WRITING INSTRUCTIONS
PRINT A1                      ;PRINT A1 USING PRINT MACRO
CALL ENTERKEY                 ;CALLING ENTERKEY PROCEDURE
PRINT A2                      ;PRINT A2 USING PRINT MACRO
CALL ENTERKEY                 ;CALLING ENTERKEY PROCEDURE
PRINT A3                      ;PRINT A3 USING PRINT MACRO
CALL ENTERKEY                 ;CALLING ENTERKEY PROCEDURE
PRINT A4                      ;PRINT A4 USING PRINT MACRO
CALL ENTERKEY                 ;CALLING ENTERKEY PROCEDURE
PRINT A5                      ;PRINT A5 USING PRINT MACRO
CALL ENTERKEY                 ;CALLING ENTERKEY PROCEDURE
PRINT A6                      ;PRINT A6 USING PRINT MACRO
CALL ENTERKEY                 ;CALLING ENTERKEY PROCEDURE
MOV AH,4CH
INT 21H
MAIN ENDP                     ;END MAIN PROCEDURE

ENTERKEY PROC                 ;CODING ENTERKEY PROCEDURE
MOV DX,10                     ;ENTER KEY INSTRUCTIONS
MOV AH,2
INT 21H
MOV DX,13
MOV AH,2
INT 21H
RET                           ;RETURN
ENTERKEY ENDP                 ;END ENTERKEY PROCEDURE
END MAIN                      ;END MAIN PROGRAM
