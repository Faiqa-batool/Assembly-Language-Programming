;PROGRAM TO TAKE INPUT OF TWO NUMBERS AND ADD THEM
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC           ;MAIN PROCEDURE STARTS
MOV AH,1
INT 21H             ;TO TAKE 1ST INPUT AND MOVE IN AH 
MOV BL,AL           ;MOVE INPUT IN BL FROM AH 
MOV AH,1
INT 21H             ;TO TAKE 2ND INPUT AND MOVE IN AH
ADD BL,AL           ;ADD THE VALUES OF BL AND AH AND MOVE IN BL
SUB BL,48           ;SUBTRACT 48(ASCII of zero) FROM THE VALUE OF BL
MOV DL,BL           ;MOVE VALUE OF BL IN DL
MOV AH,2            
INT 21H             ;TO PRINT OUTPUT (SINGLE CHARACTER)
MOV AH,4CH
INT 21H
MAIN ENDP           ;MAIN PROCEDURE ENDS
END MAIN            ;PROGRAM ENDS
