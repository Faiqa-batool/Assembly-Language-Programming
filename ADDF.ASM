;PROGRAM TO ADD TWO NUMBERS
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC       ;MAIN PROCEDURE STARTS
MOV BL,2        ;MOVE VALUE OF 2 IN BL
MOV DL,3        ;MOVE VALUE OF 3 IN DL
ADD DL,BL       ;ADD VALUES STORED IN DL AND BL
ADD DL,48
MOV AH,2
INT 21H         ;TO PRINT OUTPUT (SINGLE CHARACTER)
MOV AH,4CH
INT 21H
MAIN ENDP       ;MAIN PROCEDURE ENDS
END MAIN        ;PROGRAM ENDS
