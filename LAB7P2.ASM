;PROGRAM TO INPUT STRING AND PRINT IT:
DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
VAR1 DB 100 DUP('$')          ;INITIALIZING ARRAY OF 100 ELEMENTS OF '$'
VAR2 DB 'ENTER STRING: $'     ;INITIALIZING VARIABLES
VAR3 DB 'PRINT STRING: $'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET VAR2            ;TO PRINT 'ENTER STRING: '
MOV AH,9
INT 21H

MOV SI,OFFSET VAR1            ;MOVE ARRAY TO SOURCE INDEX REGISTER
L1:                           ;CHUNK OF CODE
MOV AH,1                      ;TAKING INPUT
INT 21H
CMP AL,13                     ;COMPARING THE INPUT WITH ASCII OF ENTER KEY
                              ;BECUASE USER ENTER THE ENTER KEY AT THE END
                              ;OF INPUT

JE PROGRAMEND                 ;JUMP TO PROGRAMEND IF EQUAL
MOV [SI],AL                   ;MOVE VALUE INTO ARRAY
INC SI                        ;INCREMENT IN SI
JMP L1                        ;JUMP TO L1

PROGRAMEND:                   ;CHUNK OF CODE
MOV DX,OFFSET VAR3            ;TO PRINT 'PRINT STRING: '
MOV AH,9
INT 21H

MOV DX,OFFSET VAR1            ;MOVE VAR1 INTO DX
MOV AH,9                      ;TO PRINT ARRAY
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
